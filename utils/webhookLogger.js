    /**
     * M√©thode de fallback utilisant les canaux classiques
     */
    async fallbackLog(type, embed, options = {}) {
        try {
            // Mapper les types sp√©cifiques aux cat√©gories principales
            let logType = type;
            if (type === 'messagesDeleted' || type === 'messagesEdited') {
                logType = 'messages';
            } else if (type.startsWith('voice_')) {
                logType = 'voice';
            } else if (type.startsWith('roles_')) {
                logType = 'roles';
            }

            const logConfig = this.logTypes[logType];
            if (!logConfig) {
                console.error(`‚ùå [WebhookLogger] Type de log inconnu pour le fallback: ${type}`);
                return;
            }
            
            const channelId = this.getConfigValue(logConfig.channelPath);
            if (!channelId) {
                console.error(`‚ùå [WebhookLogger] Aucun canal fallback configur√© pour ${logType}`);
                return;
            }

            if (!this.client) {
                console.error(`‚ùå [WebhookLogger] Client Discord non configur√© pour le fallback`);
                return;
            }

            const channel = this.client.channels.cache.get(channelId);
            if (!channel) {
                console.error(`‚ùå [WebhookLogger] Canal fallback introuvable: ${channelId}`);
                return;
            }

            // Appliquer le style du type de log si pas d√©j√† d√©fini
            if (!embed.data.color && logConfig.color) {
                embed.setColor(logConfig.color);
            }

            await channel.send({ embeds: [embed] });
            console.log(`‚úÖ [WebhookLogger] Message envoy√© en fallback dans ${channel.name} pour ${type} (via ${logType})`);
            
        } catch (error) {
            console.error(`‚ùå [WebhookLogger] Erreur fallback ${type}:`, error);
        }
    }

