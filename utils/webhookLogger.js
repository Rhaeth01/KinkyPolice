    /**
     * Utilitaire pour r√©cup√©rer une valeur depuis la config avec un chemin
     */
    getConfigValue(path) {
        try {
            const config = configManager.getConfig();
            const parts = path.split('.');
            let value = config;
            
            for (const part of parts) {
                if (value && typeof value === 'object' && part in value) {
                    value = value[part];
                } else {
                    return null;
                }
            }
            
            return value;
        } catch (error) {
            console.error(`‚ùå [WebhookLogger] Erreur r√©cup√©ration config ${path}:`, error);
            return null;
        }
    }

    /**
     * Initialise les webhooks pour tous les types de logs
     */
    async initialize(client) {
        try {
            console.log('üöÄ [WebhookLogger] Initialisation du syst√®me de webhooks...');
            
            // Stocker l'avatar du bot pour tous les webhooks
            this.botAvatar = client.user.displayAvatarURL({ size: 256 });
            
            // Mettre √† jour les avatars de tous les types de logs avec l'avatar du bot
            for (const [type, config] of Object.entries(this.logTypes)) {
                config.avatar = this.botAvatar;
            }
            
            // Initialiser les webhooks pour chaque type de log activ√©
            for (const [type, config] of Object.entries(this.logTypes)) {
                const channelId = this.getConfigValue(config.channelPath);
                
                if (!channelId) {
                    console.log(`‚ö†Ô∏è [WebhookLogger] ${type} canal non configur√©, webhook ignor√©`);
                    continue;
                }

                // Pas de r√©utilisation car chaque type a son webhook d√©di√©

                // V√©rifier que le canal existe avant d'essayer de cr√©er le webhook
                const channel = client.channels.cache.get(channelId);
                if (!channel) {
                    console.error(`‚ùå [WebhookLogger] Canal ${type} introuvable: ${channelId} - V√©rifiez que le canal existe et que le bot y a acc√®s`);
                    continue;
                }
                
                // R√©cup√©rer ou cr√©er le webhook pour ce type (mapping des noms)
                let webhookConfigKey = `logging.${type}WebhookUrl`;
                let webhookUrl;
                
                // Pour les messages, utiliser l'URL principale pour tous les variants
                if (type === 'messages') {
                    // Essayer d'abord messagesWebhookUrl, puis les variants sp√©cifiques
                    const mainMessagesUrl = this.getConfigValue('logging.messagesWebhookUrl');
                    const editedUrl = this.getConfigValue('logging.messagesEditedWebhookUrl');
                    const deletedUrl = this.getConfigValue('logging.messagesDeletedWebhookUrl');
                    
                    // Utiliser l'URL principale si les variants sont vides
                    webhookUrl = mainMessagesUrl || editedUrl || deletedUrl;
                    
                    if (mainMessagesUrl) {
                        console.log(`‚ÑπÔ∏è [WebhookLogger] Utilisation de messagesWebhookUrl pour tous les logs de messages`);
                    }
                } else {
                    webhookUrl = this.getConfigValue(webhookConfigKey);
                }
                
                if (webhookUrl) {
                    try {
                        this.webhooks.set(type, new WebhookClient({ url: webhookUrl }));
                        console.log(`‚úÖ [WebhookLogger] Webhook ${type} initialis√© depuis config (URL: ${webhookUrl.substring(0, 50)}...)`);
                    } catch (error) {
                        console.error(`‚ùå [WebhookLogger] Erreur webhook ${type}:`, error.message);
                        // Cr√©er un nouveau webhook si l'ancien est invalide
                        await this.createWebhookForType(client, type, config);
                    }
                } else {
                    // Cr√©er un nouveau webhook
                    await this.createWebhookForType(client, type, config);
                }
            }


            console.log(`üéâ [WebhookLogger] ${this.webhooks.size} webhooks initialis√©s avec succ√®s`);
            
        } catch (error) {
            console.error('‚ùå [WebhookLogger] Erreur lors de l\'initialisation:', error);
            console.log('üîÑ [WebhookLogger] Activation du mode fallback pour tous les logs');
            this.fallbackMode = true;
        }
        
        // Si aucun webhook n'a √©t√© initialis√©, utiliser le fallback
        if (this.webhooks.size === 0) {
            console.log('‚ö†Ô∏è [WebhookLogger] Aucun webhook disponible, mode fallback activ√©');
            this.fallbackMode = true;
        } else {
            console.log(`‚úÖ [WebhookLogger] Mode webhook actif avec ${this.webhooks.size} webhooks configur√©s`);
        }
    }

    /**
     * Cr√©e un webhook pour un type de log sp√©cifique
     */
    async createWebhookForType(client, type, config) {
        try {
            const channelId = this.getConfigValue(config.channelPath);
            if (!channelId) {
                console.error(`‚ùå [WebhookLogger] Aucun canal configur√© pour ${type}`);
                return;
            }

            const channel = client.channels.cache.get(channelId);
            if (!channel) {
                console.error(`‚ùå [WebhookLogger] Canal ${type} introuvable: ${channelId}`);
                return;
            }

            const webhook = await channel.createWebhook({
                name: config.name,
                avatar: this.botAvatar,
                reason: `Syst√®me de logs moderne KinkyPolice - ${config.name}`
            }).catch(error => {
                if (error.code === 30007) {
                    console.error(`‚ùå [WebhookLogger] Impossible de cr√©er webhook ${type}: Maximum number of webhooks reached (15)`);
                    console.log(`üí° [WebhookLogger] Suggestion: Supprimez des webhooks inutilis√©s ou utilisez le fallback sur canal classique`);
                } else {
                    console.error(`‚ùå [WebhookLogger] Erreur cr√©ation webhook ${type}:`, error.message);
                }
                return null;
            });

            if (!webhook) {
                return;
            }

            this.webhooks.set(type, new WebhookClient({ url: webhook.url }));
            
            // Sauvegarder automatiquement l'URL dans la configuration
            try {
                const currentConfig = configManager.getConfig();
                const updates = {
                    logging: {
                        ...currentConfig.logging,
                        [`${type}WebhookUrl`]: webhook.url
                    }
                };
                await configManager.updateConfig(updates);
                console.log(`‚úÖ [WebhookLogger] Webhook cr√©√© et URL sauvegard√©e pour ${type}: ${webhook.name}`);
            } catch (saveError) {
                console.error(`‚ùå [WebhookLogger] Erreur sauvegarde URL webhook ${type}:`, saveError.message);
                console.log(`üí° [WebhookLogger] URL √† sauvegarder manuellement: logging.${type}WebhookUrl = ${webhook.url}`);
            }
            
        } catch (error) {
            console.error(`‚ùå [WebhookLogger] Impossible de cr√©er webhook ${type}:`, error.message);
        }
    }

